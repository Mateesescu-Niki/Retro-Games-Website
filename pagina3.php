	<p>Doom (released on December 10, 1993) is the first release of the Doom series, and one of the games that consolidated the first-person shooter genre. </p>
	<br/>
	<p>With a science fiction and horror style, it gives the players the role of marines who find themselves in the focal point of an invasion from Hell. The game introduced deathmatch and cooperative play in the explicit sense, and helped further the practice of allowing and encouraging fan-made modifications of commercial video games.</p>
	<br/>
	<p>In Doom, players assume the role of an unnamed space marine, who became popularly nicknamed "Doomguy" by the community before being retconned as Doom Slayer, fighting his way through hordes of invading demons from Hell. With one third of the game, nine levels, distributed as shareware, Doom was played by an estimated 15â€“20 million people within two years of its release, popularizing the mode of gameplay and spawning a gaming subculture. In addition to popularizing the FPS genre, it pioneered immersive 3D graphics, networked multiplayer gaming, and support for customized additions and modifications via packaged files in a data archive known as "WADs". </p>
	<br/>
	<h1>Gameplay</h1>
	<br/>
	<p>The primary objective of each level is simply to locate the exit room that leads to the next area (conveniently labeled with a red EXIT sign), while surviving all hazards along the way. Among the obstacles are monsters, pits of radioactive waste, ceilings that descend to crush the player, and locked doors for which a key or remote switch need to be located. The levels are sometimes labyrinthine (the automap is a crucial aid in navigating them), and feature plenty of hidden rooms that hold powerups as a reward for players who explore thoroughly. A tally screen at the end of each level (except the last of each episode, which describes part of the plot) helps players aiming for additional objectives, such as clearing the levels of monsters or finding secret areas.</p>
	<br/>
	<p>Aside from the single-player game mode, Doom features two multiplayer modes usable over a network: co-operative mode, in which two to four players team up against the legions of hell, and deathmatch mode, in which the same number of players fight each other.</p>
	<br/>
	<p>The enemy monsters are Doom's central gameplay element. There are 10 types of monster, including possessed humans as well as demons of different strength, ranging from weaker but ubiquitous Imps and red, floating Cacodemons to the bosses, which tend to survive multiple strikes even from the player's strongest weapons. The monsters generally exhibit very simple AI, and thus most cases must outnumber the player to triumph (although great numbers can sometimes prove counterproductive due to monster infighting).</p>
	<br/>
	<p>Doom's weapon arsenal was highly distinctive in 1993 and eventually became prototypical for first-person shooters. The player starts out armed only with a pistol, and brass-knuckled fists in case his ammunition runs out, but larger weapons can be picked up: a chainsaw, a shotgun, a chaingun, a rocket launcher, a plasma gun, and the immensely powerful BFG9000. There is a wide array of additional powerups, such as a backpack that increases the player's ammunition-carrying capacity, armor, medical supplies to heal injuries, and strange alien artifacts which can turn the player invisible or boost his health and / or armor beyond its normal maximum.</p>
